from django.core.management.base import BaseCommand
from main.models import Movie


class Command(BaseCommand):
    help = 'Добавляет начальные записи о кинопроизводстве в БД'

    def handle(self, *args, **options):
        movies_data = [
            {
                'title': 'Марсианская одиссея',
                'content': 'Эпическая история о первой пилотируемой экспедиции на Марс. Команда из шести астронавтов сталкивается с непредвиденными опасностями на красной планете, когда их модуль приземления терпит крушение. Им предстоит найти способ выжить и вернуться домой, преодолевая суровые условия марсианской среды.'
            },
            {
                'title': 'Код вечности',
                'content': 'Научно-фантастический триллер о программисте, который обнаруживает загадочный код, способный предсказывать будущее. Герой оказывается в центре глобального заговора, когда понимает, что его открытие может изменить ход истории человечества.'
            },
            {
                'title': 'Тайны спецэффектов',
                'content': 'Документальный фильм о развитии технологий спецэффектов в современном кинематографе. Зрители увидят закулисье создания блокбастеров и узнают, как компьютерная графика совершила революцию в индустрии развлечений.'
            },
        ]

        for movie_data in movies_data:
            movie = Movie(**movie_data)
            movie.save()
            self.stdout.write(
                self.style.SUCCESS(f'Создан фильм: {movie.title} (ID: {movie.id})')
            )

        self.stdout.write(
            self.style.SUCCESS(f'\nВсего создано записей: {len(movies_data)}')
        )
        
        # Вывод всех записей
        self.stdout.write('\n=== Все записи в БД ===')
        for movie in Movie.objects.all():
            self.stdout.write(f'ID: {movie.id}, Title: {movie.title}')
